# 商品番号、商品名、入荷数、単価を入力として受け取り、商品番号、商品名、入荷数、単価、金額をcsvファイルに出力するプログラム
# 金額は入荷数と単価の積で計算する
# 金額は、単価の桁数に応じて、右寄せで出力する
# 金額の桁数は、単価の桁数に応じて、単価の桁数+3桁にする
# 金額の桁数は、単価の桁数に応じて、単価の桁数+3桁にする

import csv

# ファイルを開く
with open('test.csv', 'w', newline='') as f:
    # writerオブジェクトを作成
    writer = csv.writer(f)
    # writerow()メソッドで1行ずつ書き込む
    writer.writerow(['商品番号', '商品名', '入荷数', '単価', '金額'])
    writer.writerow(['1001', 'りんご', '100', '100', '10000'])
    writer.writerow(['1002', 'みかん', '200', '200', '40000'])
    writer.writerow(['1003', 'ぶどう', '300', '300', '90000'])
    writer.writerow(['1004', 'もも', '400', '400', '160000'])
    writer.writerow(['1005', 'なし', '500', '500', '250000'])
    writer.writerow(['1006', 'さくらんぼ', '600', '600', '360000'])
    writer.writerow(['1007', 'いちご', '700', '700', '490000'])
    writer.writerow(['1008', 'すいか', '800', '800', '640000'])
    writer.writerow(['1009', 'メロン', '900', '900', '810000'])
    writer.writerow(['1010', 'パイナップル', '1000', '1000', '1000000'])

# ファイルを開く
with open('test.csv', 'r') as f:
    # readerオブジェクトを作成
    reader = csv.reader(f)
    # readerオブジェクトから1行ずつ読み込む
    for row in reader:
        # 読み込んだ1行を出力
        print(row)

